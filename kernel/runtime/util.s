#define GSBASE 0xC0000101
#define STACKFLAGS 3
#define STACK 0x80000

TEXT main·invlpg(SB), $0
	MOVQ 8(SP), AX
	BYTE $0x0F // INVLPG (AX)
	BYTE $0x01
	BYTE $0x38
	RET

TEXT main·setcr3(SB), $0
	MOVQ 8(SP), AX
	MOVQ AX, CR3
	RET

TEXT main·lgdt(SB), $10
	MOVQ addr+0(FP), AX
	MOVQ AX, 2(SP)
	MOVW len+8(FP), AX
	SHLW $3, AX
	MOVW AX, 0(SP)
	BYTE $0x0F // LGDT (SP)
	BYTE $0x01
	BYTE $0x14
	BYTE $0x24
	RET

TEXT main·lidt(SB), $10
	MOVQ addr+0(FP), AX
	MOVQ AX, 2(SP)
	MOVW $4096, 0(SP)
	BYTE $0x0F // LIDT (SP)
	BYTE $0x01
	BYTE $0x1C
	BYTE $0x24
	RET

TEXT main·loadsegs(SB), $0
	MOVL $16, BX
	MOVW BX, DS
	MOVW BX, ES
	MOVW BX, SS
	SUBQ $8, SP
	MOVQ 8(SP), AX
	MOVQ AX, 0(SP)
	MOVL $8, 8(SP)
	BYTE $0x48 // RETF
	BYTE $0xCB

TEXT main·outb(SB), $0
	MOVB data+2(FP), AX
	MOVW addr+0(FP), DX
	BYTE $0xEE // OUTB AX, DX
	RET

TEXT main·halt(SB), $0
	HLT

TEXT main·cli(SB), $0
	CLI
	RET

TEXT main·sti(SB), $0
	STI
	RET

TEXT main·memzero(SB), $0
	MOVQ addr+0(FP), DI
	MOVQ size+8(FP), CX
	XORQ AX, AX
	REP; STOSB
	RET

TEXT main·memcpy(SB), $0
	MOVQ dst+0(FP), DI
	MOVQ src+8(FP), SI
	MOVQ n+16(FP), CX
	REP; MOVSB
	RET

TEXT main·commonisraddr(SB), $0
	MOVQ $main·common_isr(SB), 8(SP)
	RET

