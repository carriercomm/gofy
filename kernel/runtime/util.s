TEXT runtime·new(SB), $16
	MOVQ n+0(FP), AX
	MOVQ AX, 0(SP)
	CALL main·kmalloc(SB)
	MOVQ 8(SP), AX
	MOVQ AX, addr+8(FP)
	MOVQ AX, 0(SP)
	MOVQ n+0(FP), AX
	MOVQ AX, 8(SP)
	CALL main·memzero(SB)
	RET

TEXT main·savu(SB), $0
	MOVQ c+0(FP), BX
	MOVQ (SP), DI
	ADDQ $8, SP
	MOVQ SP, (BX)
	MOVL $GSBASE, CX
	RDMSR
	MOVL AX, 8(BX)
	MOVL DX, 12(BX)
	JMP *DI

